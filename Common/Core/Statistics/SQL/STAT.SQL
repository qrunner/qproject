CREATE TABLE SOZ_STAT (
  POOL_ID NUMBER(10) NOT NULL,
  THREADS NUMBER(10) NOT NULL,
  INPROCESS NUMBER(10) NOT NULL,
  INQUEUE NUMBER(10) NOT NULL,
  STATUS NUMBER(1) NOT NULL,
  PROCESSED NUMBER(10) NOT NULL,
  ERRORS NUMBER(10) NOT NULL,
  PROCESS_TIME NUMBER(10) NOT NULL,
  FOREIGN_TIME NUMBER(10) NOT NULL,
  PICKED NUMBER(10) NOT NULL,
  PICK_ERRORS NUMBER(10) NOT NULL,
  ENQUEUED NUMBER(10) NOT NULL,
  DTM TIMESTAMP(6) NOT NULL,
  PICK_TIME NUMBER(10) NOT NULL,
  PICKS_COUNT NUMBER(10) NOT NULL,
  SERVER_ID NUMBER(10) NOT NULL)
TABLESPACE DIA_DATA
STORAGE (
  INITIAL 64K
  MAXEXTENTS UNLIMITED
)
LOGGING;

CREATE INDEX UK_SOZ_SERV_STAT ON SOZ_STAT (DTM, POOL_ID)
TABLESPACE DIA_DATA
STORAGE (
  INITIAL 64K
  MAXEXTENTS UNLIMITED
)
LOGGING;

COMMENT ON COLUMN SOZ_STAT.DTM IS 'Record Timestamp';
COMMENT ON COLUMN SOZ_STAT.ENQUEUED IS 'Enqueued Count';
COMMENT ON COLUMN SOZ_STAT.ERRORS IS 'Errors Count';
COMMENT ON COLUMN SOZ_STAT.FOREIGN_TIME IS 'Foreign System Time';
COMMENT ON COLUMN SOZ_STAT.INPROCESS IS 'In Process Count';
COMMENT ON COLUMN SOZ_STAT.INQUEUE IS 'In Queue Count';
COMMENT ON COLUMN SOZ_STAT.PICK_ERRORS IS 'Pick Errors';
COMMENT ON COLUMN SOZ_STAT.PICK_TIME IS 'Pick Objects Time';
COMMENT ON COLUMN SOZ_STAT.PICKED IS 'Picked Count';
COMMENT ON COLUMN SOZ_STAT.PICKS_COUNT IS 'Count of Picks';
COMMENT ON COLUMN SOZ_STAT.POOL_ID IS 'Pool Identifier';
COMMENT ON COLUMN SOZ_STAT.PROCESS_TIME IS 'Processing Time';
COMMENT ON COLUMN SOZ_STAT.PROCESSED IS 'Processed Count';
COMMENT ON COLUMN SOZ_STAT.SERVER_ID IS 'Server identifier';
COMMENT ON COLUMN SOZ_STAT.STATUS IS 'Pool State';
COMMENT ON COLUMN SOZ_STAT.THREADS IS 'Threads Count'